--10
SELECT _FILMES.Nome, _GENEROS.Genero FROM Filmes _FILMES
JOIN FilmesGenero _FILME_GENERO ON _FILMES.Id = _FILME_GENERO.IdFilme
JOIN Generos _GENEROS ON _FILME_GENERO.IdGenero = _GENEROS.Id

--11
SELECT _FILMES.Nome, _GENEROS.Genero FROM Filmes _FILMES
JOIN FilmesGenero _FILME_GENERO ON _FILMES.Id = _FILME_GENERO.IdFilme
JOIN Generos _GENEROS ON _FILME_GENERO.IdGenero = _GENEROS.Id
WHERE _GENEROS.Genero = 'Mistério'

--12
SELECT _FILMES.Nome,_ATORES.PrimeiroNome, _ATORES.UltimoNome, _ELENCO_FILMES.Papel FROM Filmes _FILMES
JOIN ElencoFilme _ELENCO_FILMES ON _FILMES.Id = _ELENCO_FILMES.IdFilme
JOIN Atores _ATORES ON _ELENCO_FILMES.IdAtor = _ATORES.Id

