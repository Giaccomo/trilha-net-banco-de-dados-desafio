--1
SELECT NOME, ANO FROM Filmes

--2
SELECT NOME, ANO FROM Filmes
ORDER BY ANO ASC

--3
SELECT NOME, ANO, DURACAO FROM Filmes
WHERE Nome LIKE 'DE VOLTA%'

--4
SELECT * FROM Filmes
WHERE ANO = 1997

--5
SELECT * FROM Filmes
WHERE ANO > 2000

--6
SELECT * FROM Filmes
WHERE DURACAO > 100 AND DURACAO < 150
ORDER BY Duracao ASC

--7 
/* OBS.: NO ENUNCIADO PEDE PARA ORDENAR PELA DURAÇÃO DESC, PORÉM PARA TER O MESMO RESULTADO
   DA IMAGEM, ORDENEI PELO TOTAL
*/
SELECT ANO, COUNT(*) AS TOTAL FROM Filmes
GROUP BY ANO
ORDER BY (TOTAL) DESC

